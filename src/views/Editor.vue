<script>
import MenuBubble from "@/components/MenuBubble.vue";
import { mapState } from "vuex";

export default {
  name: "Home",

  components: {
    MenuBubble
  },

  data: () => ({
    //? data
    draggable: true,
    rotatable: false,
    screen: "",
    curtain: () => {
      if (window.innerWidth == 1920) {
        return `width: 0px; z-index: 11;`;
      } else {
        return `width: ${(window.innerWidth - 1920) / 2 - 9}px; z-index: 11;`;
      }
    },
    mode: "desktop",
    back: [],
    back_rows: 30,
    back_letters_in_row: 150,

    //? element data
    style: ""
  }),

  methods: {
    uuid: function() {
      var uuidValue = "",
        k,
        randomValue;
      for (k = 0; k < 32; k++) {
        randomValue = (Math.random() * 16) | 0;

        if (k == 8 || k == 12 || k == 16 || k == 20) {
          uuidValue += "-";
        }
        uuidValue += (k == 12
          ? 4
          : k == 16
          ? (randomValue & 3) | 8
          : randomValue
        ).toString(16);
      }
      return uuidValue;
    },

    randomN: function(min, max) {
      let rand = Math.random() * (max - min) + min;
      return Math.round(rand);
    },

    setTransform: function(transform) {
      switch (transform) {
        case "drag":
          this.draggable = true;
          this.rotatable = false;
          break;

        case "rotate":
          this.draggable = false;
          this.rotatable = true;
          break;

        default:
          break;
      }
    },

    setScreentype: function(screen) {
      switch (screen) {
        case "phone":
          this.style = this.$store.state.elements[
            this.currentElement
          ].transform_object["phone"].style;
          this.mode = "phone";
          this.screen = "width: 600px;";
          this.curtain = `width: ${(window.innerWidth - 600) / 2 -
            9}px; z-index: 11;`;
          break;

        case "laptop":
          this.style = this.$store.state.elements[
            this.currentElement
          ].transform_object["laptop"].style;
          this.mode = "laptop";
          this.screen = "width: 1280px;";
          this.curtain = `width: ${(window.innerWidth - 1280) / 2 -
            9}px; z-index: 11;`;
          break;

        case "desktop":
          this.style = this.$store.state.elements[
            this.currentElement
          ].transform_object["desktop"].style;
          this.mode = "desktop";
          this.screen = "width: 1920px;";
          if (window.innerWidth == 1920) {
            this.curtain = `width: 0px; z-index: 11;`;
          } else {
            this.curtain = `width: ${(window.innerWidth - 1920) / 2 -
              9}px; z-index: 11;`;
          }
          break;
      }
    },

    addElement: function() {
      let payload = {
        //? id & currentElement
        id: this.numberOfElements,
        currentElement: true,

        //? string variables for transform
        translate_string: "",
        rotate_string: "",
        scale_string: "",
        style_string: "",

        //? service variables
        is_drag: null,
        mode: null,
        is_resize: null,
        is_rotate: null,
        transform_object: {
          phone: {
            translate: {
              x: null,
              y: null
            },
            rotate: null,
            style: null,
            content: null
          },
          laptop: {
            translate: {
              x: null,
              y: null
            },
            rotate: null,
            style: null,
            content: null
          },
          desktop: {
            translate: {
              x: null,
              y: null
            },
            rotate: null,
            style: null,
            content: null
          }
          //translate: {
          //  phone: {
          //    x: null,
          //    y: null
          //  },
          //  laptop: {
          //    x: null,
          //    y: null
          //  },
          //  desktop: {
          //    x: null,
          //    y: null
          //  }
          //},
          //rotate: {
          //  phone: null,
          //  laptop: null,
          //  desktop: null
          //},
          //scale: null,
          //resize: {
          //  width: null,
          //  height: null
          //}
        },

        //? drag variables
        dragActive: false,
        onElement: false,
        currentX: null,
        currentY: null,
        initialX: null,
        initialY: null,
        xOffset: 0,
        yOffset: 0,
        transition: null,

        //? rotate variables
        active: false,
        angle: 0,
        rotation: 0,
        startAngle: 0
      };
      this.$store.commit("addElement", payload);
    },
    deleteElement: function() {
      this.$store.state.elements.splice(this.currentElement);
    },
    setCurrent(id) {
      this.$store.commit("setCurrentElement", id);
    }
  },

  computed: {
    screen_type: function() {
      let screen;
      switch (this.$vuetify.breakpoint.name) {
        case "xs":
          screen = "phone";
          break;
        case "sm":
          screen = "tablet";
          break;
        case "md":
          screen = "laptop";
          break;
        case "lg":
          screen = "desktop";
          break;
        case "xl":
          screen = "4k";
          break;
      }
      return screen;
    },

    elements: function() {
      return this.$store.getters.getElements;
    },
    numberOfElements: function() {
      return this.$store.getters.getNumberElements;
    },
    ...mapState(["currentElement"]),
    style: {
      get: function() {
        if (this.currentElement == undefined || this.currentElement == null) {
          return this.$store.state.elements[this.currentElement]
            .transform_object[this.mode].style;
        } else {
          return null;
        }
      },
      set: function(newValue) {
        this.$store.state.elements[this.currentElement].transform_object[
          this.mode
        ].style = newValue;
      }
    }
  },

  watch: {
    style(newValue) {
      let mode = this.mode;
      let el = this.currentElement;
      this.$store.state.elements[el].transform_object[mode].style = newValue;
      console.log("new style!");
    },
    currentElement(newValue) {
      let mode = this.mode;
      let el = newValue;
      this.style = this.$store.state.elements[el].transform_object[mode].style;
    },
    mode(newValue) {
      this.$store.state.currentMode = newValue;
    }
  },
  created() {
    function setProperty(duration, red, blue, green) {
      duration = duration * 10;
      document.body.style.setProperty("--animation-time", duration + "s");
      document.body.style.setProperty(
        "--color",
        "rgba(" + red + ", " + blue + ", " + green + ", 1)"
      );
    }
    function changeAnimationTime() {
      const animationDuration = Math.floor(Math.random(20));
      const color_red = Math.floor(Math.random(255) * 100);
      const color_blue = Math.floor(Math.random(255) * 100);
      const color_green = Math.floor(Math.random(255) * 100);
      setProperty(animationDuration, color_red, color_blue, color_green);
    }
    setInterval(changeAnimationTime, 10000);
  }
};
